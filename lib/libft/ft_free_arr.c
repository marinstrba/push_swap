#include "libft.h"

void	ft_free_arr(char **arr)
{
	int	i;

	i = 0;
	if (arr == NULL)
		return ;
	while (arr[i] != NULL)
	{
		free(arr[i]);
		++i;
	}
	free(arr);
}
